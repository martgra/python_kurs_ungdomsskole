# Demonstrasjon av ulike aritmetiske operatorer

svar_addisjon = 1 + 1
print(f"Svar addisjon: {svar_addisjon}")

svar_subtraksjon = 2 - 1
print(f"Svar subtraksjon: {svar_subtraksjon}")

svar_multiplikasjon = 2 * 4
print(f"Svar multiplikasjon: {svar_multiplikasjon}")

svar_divisjon = 5 / 2
print(f"Svar divisjon: {svar_divisjon}")

svar_modulus = 5 % 4
print(f"Svar modulus: {svar_modulus}")

svar_eksponent = 10**2
print(f"Svar eksponent: {svar_eksponent}")

svar_gulvdivisjon = 5 // 2
print(f"Svar gulvdivisjon: {svar_gulvdivisjon}")